#include <iostream>

int main() {
    int a;
    int b;
    int c;

    std::cin >> a >> b >> c;

    int d = a - b - c;
    int e = b * c - a;
    int f = 2 * b - c + 5 * a;

    std::cout << d << "\n";
    std::cout << e << "\n";
    std::cout << f << "\n";

    return 0;
}





















































































